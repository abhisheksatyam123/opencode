PART_MAPPING["tool"] = function ToolPartDisplay(props) {
  const data = useData()
  const part = () => props.part as ToolPart

  const hideQuestion = createMemo(() => false)

  const emptyInput: Record<string, any> = {}
  const emptyMetadata: Record<string, any> = {}

  const input = () => part().state.input ?? emptyInput
  const partMetadata = () => {
    const state = part().state
    if ("metadata" in state) return state.metadata ?? emptyMetadata
    return part().metadata ?? emptyMetadata
  }
  const output = () => {
    const state = part().state
    return state.status === "completed" ? state.output : undefined
  }
  const taskId = createMemo(() => {
    if (part().tool !== "task") return
    const value = partMetadata().sessionId
    if (typeof value === "string" && value) return value
  })
  const taskHref = createMemo(() => {
    if (part().tool !== "task") return
    return sessionLink(taskId(), useLocation().pathname, data.sessionHref)
  })
  const taskSubtitle = createMemo(() => {
    if (part().tool !== "task") return undefined
    const value = input().description
    if (typeof value === "string" && value) return value
    return taskId()
  })

  const render = createMemo(() => ToolRegistry.render(part().tool) ?? GenericTool)

  const stateError = createMemo(() => {
    const state = part().state as unknown
    if (!state || typeof state !== "object") return undefined
    if (!("status" in state) || state.status !== "error") return undefined
    const error = "error" in state ? state.error : undefined
    return typeof error === "string" ? error : undefined
  })

  return (
    <Show when={!hideQuestion()}>
      <div data-component="tool-part-wrapper">
        <Switch>
          <Match when={stateError()}>
            {(error) => {
              return (
                <ToolErrorCard
                  tool={part().tool}
                  error={error()}
                  defaultOpen={props.defaultOpen}
                  subtitle={taskSubtitle()}
                  href={taskHref()}
                />
              )
            }}
          </Match>
          <Match when={true}>
            <Dynamic
              component={render()}
              input={input()}
              tool={part().tool}
              sessionID={part().sessionID}
              metadata={partMetadata()}
              output={output()}
              status={part().state.status}
              hideDetails={props.hideDetails}
              defaultOpen={props.defaultOpen}
            />
          </Match>
        </Switch>
      </div>
    </Show>
  )
}

