export function isCommandSafeViaFlagParsing(command: string): boolean {
  let rest = command.trim()

  // Strip leading env vars: VAR=value patterns
  const envVarPattern = /^[A-Z_][A-Z0-9_]*=\S*\s+/
  while (envVarPattern.test(rest)) {
    rest = rest.replace(envVarPattern, "")
  }

  const tokens = rest.split(/\s+/).filter((t) => t.length > 0)
  if (tokens.length === 0) return false

  const baseCmd = tokens[0]
  const config = COMMAND_CONFIGS[baseCmd]
  if (!config) return false
  if (config.alwaysUnsafe) return false

  const flagTokens = tokens.slice(1).filter((t) => t.startsWith("-"))

  if (config.mutatingFlags) {
    for (const flag of flagTokens) {
      if (config.mutatingFlags.has(flag)) return false
    }
  }

  for (const flag of flagTokens) {
    if (!config.safeFlags.has(flag)) return false
  }

  return true
}
